def get_rubric_by_id(rubric_id: int):
    """Получить рубрикатор по ID"""
    for rubric in RUBRICS:
        if rubric["id"] == rubric_id:
            return rubric
    return None


def get_all_rubrics():
    """Получить все рубрикаторы"""
    return RUBRICS


def get_rubric_names():
    """Получить список названий рубрикаторов"""
    return [rubric["name"] for rubric in RUBRICS]


def get_rubric_keywords(rubric_id: int):
    """Получить ключевые слова для рубрикатора"""
    rubric = get_rubric_by_id(rubric_id)
    return rubric["keywords"] if rubric else []
