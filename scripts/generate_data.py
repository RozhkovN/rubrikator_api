def generate_complaint(rubric_id: int, style: str) -> str:
    """–ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –æ–¥–Ω—É –∂–∞–ª–æ–±—É –¥–ª—è —É–∫–∞–∑–∞–Ω–Ω–æ–≥–æ —Ä—É–±—Ä–∏–∫–∞—Ç–æ—Ä–∞"""
    examples = RUBRIC_EXAMPLES.get(rubric_id)
    if not examples:
        return ""
    
    import random
    template = random.choice(TEMPLATES[style])
    issue = random.choice(examples["issues"])
    detail = random.choice(examples["details"])
    
    complaint = template.format(issue=issue, details=detail)
    return complaint


def generate_training_data(samples_per_rubric: int = 15) -> List[Dict]:
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –æ–±—É—á–∞—é—â–∏–µ –¥–∞–Ω–Ω—ã–µ –¥–ª—è –≤—Å–µ—Ö —Ä—É–±—Ä–∏–∫–∞—Ç–æ—Ä–æ–≤.
    
    Args:
        samples_per_rubric: –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø—Ä–∏–º–µ—Ä–æ–≤ –Ω–∞ –∫–∞–∂–¥—ã–π —Ä—É–±—Ä–∏–∫–∞—Ç–æ—Ä
        
    Returns:
        –°–ø–∏—Å–æ–∫ —Å–ª–æ–≤–∞—Ä–µ–π —Å –ø–æ–ª—è–º–∏: text, rubric_id, rubric_name, style
    """
    training_data = []
    
    for rubric in RUBRICS:
        rubric_id = rubric["id"]
        rubric_name = rubric["name"]
        
        # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –ø—Ä–∏–º–µ—Ä—ã –≤ —Ä–∞–∑–Ω—ã—Ö —Å—Ç–∏–ª—è—Ö
        styles_count = {
            "official": samples_per_rubric // 3,
            "free": samples_per_rubric // 3,
            "emotional": samples_per_rubric - (2 * (samples_per_rubric // 3))
        }
        
        for style, count in styles_count.items():
            for _ in range(count):
                complaint_text = generate_complaint(rubric_id, style)
                training_data.append({
                    "text": complaint_text,
                    "rubric_id": rubric_id,
                    "rubric_name": rubric_name,
                    "style": style
                })
    
    return training_data


def save_training_data(data: List[Dict], filepath: str = "data/training_data.json"):
    """–°–æ—Ö—Ä–∞–Ω—è–µ—Ç –æ–±—É—á–∞—é—â–∏–µ –¥–∞–Ω–Ω—ã–µ –≤ JSON —Ñ–∞–π–ª"""
    os.makedirs(os.path.dirname(filepath), exist_ok=True)
    
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    print(f"‚úì –°–æ—Ö—Ä–∞–Ω–µ–Ω–æ {len(data)} –ø—Ä–∏–º–µ—Ä–æ–≤ –≤ {filepath}")


def load_training_data(filepath: str = "data/training_data.json") -> List[Dict]:
    """–ó–∞–≥—Ä—É–∂–∞–µ—Ç –æ–±—É—á–∞—é—â–∏–µ –¥–∞–Ω–Ω—ã–µ –∏–∑ JSON —Ñ–∞–π–ª–∞"""
    with open(filepath, 'r', encoding='utf-8') as f:
        data = json.load(f)
    return data


